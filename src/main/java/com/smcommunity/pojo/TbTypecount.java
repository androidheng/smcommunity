public class TbTypecount {
    private Integer uid;

    private Double food;

    private Double electronics;

    private Double everyday;

    private Double book;

    private Double clothing;

    private Double fresh;

    public Integer getUid() {
        return uid;
    }

    public void setUid(Integer uid) {
        this.uid = uid;
    }

    public Double getFood() {
        return food;
    }

    public void setFood(Double food) {
        this.food = food;
    }

    public Double getElectronics() {
        return electronics;
    }

    public void setElectronics(Double electronics) {
        this.electronics = electronics;
    }

    public Double getEveryday() {
        return everyday;
    }

    public void setEveryday(Double everyday) {
        this.everyday = everyday;
    }

    public Double getBook() {
        return book;
    }

    public void setBook(Double book) {
        this.book = book;
    }

    public Double getClothing() {
        return clothing;
    }

    public void setClothing(Double clothing) {
        this.clothing = clothing;
    }

    public Double getFresh() {
        return fresh;
    }

    public void setFresh(Double fresh) {
        this.fresh = fresh;
    }
}